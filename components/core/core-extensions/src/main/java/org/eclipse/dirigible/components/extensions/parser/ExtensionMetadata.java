package org.eclipse.dirigible.components.extensions.parser;

/**
 * Simple metadata holder for @Extension-decorated classes.
 */
public class ExtensionMetadata {

    private String className;
    private String name;
    private String to;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    @Override
    public String toString() {
        return "ExtensionMetadata{" + "className='" + className + '\'' + ", name='" + name + '\'' + ", to='" + to + '\'' + '}';
    }
}
