package org.eclipse.dirigible.components.websockets.parser;

/**
 * Simple metadata holder for @Websocket-decorated classes.
 */
public class WebsocketMetadata {

    private String className;
    private String name;
    private String endpoint;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEndpoint() {
        return endpoint;
    }

    public void setEndpoint(String endpoint) {
        this.endpoint = endpoint;
    }

    @Override
    public String toString() {
        return "WebsocketMetadata{" + "className='" + className + '\'' + ", name='" + name + '\'' + ", endpoint='" + endpoint + '\'' + '}';
    }
}
